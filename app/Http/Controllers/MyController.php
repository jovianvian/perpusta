    public function revertEdit($id)
    {
        if (!$this->isSuperAdminSession()) return view('404');

        $history = DB::table('edit_histories')->where('id', $id)->first();
        if (!$history || !$history->old_values) {
            return back()->with('error', 'Data riwayat tidak dapat direvert.');
        }

        $oldValues = json_decode($history->old_values, true);
        if (!$oldValues || !is_array($oldValues)) {
            return back()->with('error', 'Data lama tidak tersedia.');
        }

        $table = $history->table_name;
        $rowId = $history->row_id;

        if (!in_array($table, ['books', 'data_masuk_buku', 'peminjaman_buku', 'users'])) {
            return back()->with('error', 'Tabel tidak didukung untuk revert.');
        }

        $current = DB::table($table)->where('id', $rowId)->first();
        if (!$current) {
            return back()->with('error', 'Data sumber tidak ditemukan.');
        }

        $currentArray = [];
        foreach ($oldValues as $key => $value) {
            if (property_exists($current, $key)) {
                $currentArray[$key] = $current->$key;
            }
        }

        $beforeValues = $currentArray;
        $afterValues = $oldValues;

        $perubahanText = 'Revert ke versi sebelumnya';
        $detailChanges = [];

        foreach ($afterValues as $key => $afterVal) {
            $beforeVal = array_key_exists($key, $beforeValues) ? $beforeValues[$key] : null;
            if ((string)$beforeVal === (string)$afterVal) {
                continue;
            }

            // Simple label mapping
            $label = ucfirst($key);
            $beforeText = $beforeVal === null || $beforeVal === '' ? '-' : (string)$beforeVal;
            $afterText = $afterVal === null || $afterVal === '' ? '-' : (string)$afterVal;
            $detailChanges[] = $label . ': ' . $beforeText . ' → ' . $afterText;
        }

        if (!empty($detailChanges)) {
            $perubahanText = 'Revert: ' . implode('; ', $detailChanges);
        } else {
            $perubahanText = 'Revert ke versi sebelumnya (tanpa perubahan nilai)';
        }

        DB::table($table)->where('id', $rowId)->update($oldValues);

        DB::table('edit_histories')->insert([
            'table_name' => $table,
            'row_id' => $rowId,
            'perubahan' => $perubahanText,
            'old_values' => json_encode($beforeValues),
            'new_values' => json_encode($afterValues),
            'edited_by' => session('id'),
            'created_at' => now(),
            'updated_at' => now()
        ]);

        return back()->with('success', 'Data berhasil dikembalikan ke versi sebelumnya.');
    }

    public function restore($type, $id)
    {
        if (!$this->isSuperAdminSession()) return view('404');

        $table = '';
        if ($type == 'book') $table = 'books';
        elseif ($type == 'user') $table = 'users';
        elseif ($type == 'peminjaman') $table = 'peminjaman_buku';
        elseif ($type == 'datamasuk') $table = 'data_masuk_buku';

        if ($table) {
            DB::table($table)->where('id', $id)->update(['deleted_at' => null]);
            return back()->with('success', 'Data berhasil dikembalikan!');
        }
        return back()->with('error', 'Tipe data tidak valid');
    }

    public function forceDelete($type, $id)
    {
        if (!$this->isSuperAdminSession()) return view('404');

        $table = '';
        if ($type == 'book') $table = 'books';
        elseif ($type == 'user') $table = 'users';
        elseif ($type == 'peminjaman') $table = 'peminjaman_buku';
        elseif ($type == 'datamasuk') $table = 'data_masuk_buku';

        if ($table) {
            DB::table($table)->where('id', $id)->delete();
            // If user, also delete related
            if ($type == 'user') {
                DB::table('penjagas')->where('user_id', $id)->delete();
                DB::table('peminjams')->where('user_id', $id)->delete();
            }
            return back()->with('success', 'Data dihapus permanen!');
        }
        return back()->with('error', 'Tipe data tidak valid');
    }

