    private function isSuperAdminSession(): bool
    {
        $userId = session('id');
        if (!$userId) {
            return false;
        }

        $user = DB::table('users')->where('id', $userId)->first();
        return $user && in_array((int) $user->level_id, [5, 6], true);
    }

    // ------------------- GLOBAL SEARCH -------------------
    public function search(Request $request)
    {
        $q = $request->q;
        
        // Search Books
        $books = DB::table('books')
            ->whereNull('deleted_at')
            ->where('judul', 'like', "%$q%")
            ->get();
            
        // Search Users
        $users = DB::table('users')
            ->whereNull('deleted_at')
            ->where(function($query) use ($q) {
                $query->where('name', 'like', "%$q%")
                      ->orWhere('email', 'like', "%$q%");
            })
            ->get();
            
        // Search Loans
        $loans = DB::table('peminjaman_buku')
            ->join('books', 'peminjaman_buku.book_id', '=', 'books.id')
            ->join('users', 'peminjaman_buku.user_id', '=', 'users.id')
            ->select('peminjaman_buku.*', 'books.judul', 'users.name')
            ->whereNull('peminjaman_buku.deleted_at')
            ->where(function($query) use ($q) {
                $query->where('books.judul', 'like', "%$q%")
                      ->orWhere('users.name', 'like', "%$q%");
            })
            ->get();

        if ($request->ajax() || $request->wantsJson()) {
            return response()->json([
                'books' => $books,
                'users' => $users,
                'loans' => $loans,
                'count' => $books->count() + $users->count() + $loans->count()
            ]);
        }

        return view('search_results', compact('books', 'users', 'loans', 'q'));
    }

