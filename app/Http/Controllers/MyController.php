                    ->whereYear('tanggal_pinjam', now()->year)
                    ->whereNull('deleted_at')
                    ->groupBy('month')
                    ->pluck('count', 'month')
                    ->toArray();
                
                $chart_data = array_fill(0, 12, 0);
                foreach ($monthly_loans as $month => $count) {
                    $chart_data[$month - 1] = $count;
                }

                // Daily Loans Global (Last 7 Days)
                $daily_loans = DB::table('peminjaman_buku')
                    ->select(DB::raw('COUNT(id) as count'), DB::raw('DATE(tanggal_pinjam) as date'))
                    ->where('tanggal_pinjam', '>=', now()->subDays(7))
                    ->whereNull('deleted_at')
                    ->groupBy('date')
                    ->pluck('count', 'date')
