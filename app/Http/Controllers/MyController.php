                    ->whereNull('deleted_at')
                    ->groupBy('month')
                    ->pluck('count', 'month')
                    ->toArray();
                
                $chart_data = array_fill(0, 12, 0);
                foreach ($monthly_loans as $month => $count) {
                    $chart_data[$month - 1] = $count;
                }

                return view('home', compact('my_active_loans', 'my_total_loans', 'my_returned_loans', 'recent_activities', 'chart_data'));
            } 
            
            // --- ADMIN / SUPER ADMIN DASHBOARD ---
