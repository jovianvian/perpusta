                $total_users = DB::table('users')->whereNull('deleted_at')->count();
                $active_loans = DB::table('peminjaman_buku')->where('status', 'dipinjam')->whereNull('deleted_at')->count();
                $today_loans = DB::table('peminjaman_buku')->whereDate('tanggal_pinjam', now())->whereNull('deleted_at')->count();

