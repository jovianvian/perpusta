    public function backup()
    {
        // Only Super Admin
        $userId = session('id');
        $user = DB::table('users')->where('id', $userId)->first();
        if (!$user || ($user->level_id != 5 && $user->level_id != 6)) {
            return abort(403);
        }

        // Expanded MySQL Dump Logic
        $filename = 'backup-' . date('Y-m-d-H-i-s') . '.sql';
        
        // Get all tables dynamically
        $allTables = DB::select('SHOW TABLES');
        $tables = [];
        foreach ($allTables as $table) {
            foreach ($table as $key => $value) {
                $tables[] = $value;
            }
        }

        // Start constructing the content
        $content = "-- Backup Generated by Library System\n";
        $content .= "-- Date: " . now() . "\n";
        $content .= "-- --------------------------------------------------------\n\n";
        $content .= "SET FOREIGN_KEY_CHECKS=0;\n";
        $content .= "SET SQL_MODE = \"NO_AUTO_VALUE_ON_ZERO\";\n\n";
        
        foreach ($tables as $table) {
            // Get Create Table Syntax
            $createTable = DB::select("SHOW CREATE TABLE `$table`");
            $createTableSql = $createTable[0]->{'Create Table'};

            $content .= "-- Table structure for table `$table`\n";
            $content .= "DROP TABLE IF EXISTS `$table`;\n";
            $content .= $createTableSql . ";\n\n";

            // Get Data
            $rows = DB::table($table)->get();
            
            if ($rows->count() > 0) {
                $content .= "-- Dumping data for table `$table`\n";
                $content .= "INSERT INTO `$table` VALUES \n";
                
                $valuesArr = [];
                foreach ($rows as $row) {
                    $rowArray = (array)$row;
                    $rowValues = [];
                    foreach ($rowArray as $value) {
                        if ($value === null) {
                            $rowValues[] = "NULL";
                        } else {
                            $rowValues[] = "'" . addslashes($value) . "'";
                        }
                    }
                    $valuesArr[] = "(" . implode(', ', $rowValues) . ")";
                }
                
                $content .= implode(",\n", $valuesArr);
                $content .= ";\n\n";
            }
        }

        $content .= "SET FOREIGN_KEY_CHECKS=1;\n";

        return response()->streamDownload(function () use ($content) {
            echo $content;
        }, $filename);
    }

    public function resetDatabase()
    {
        // Only Super Admin
        $userId = session('id');
        $user = DB::table('users')->where('id', $userId)->first();
        if (!$user || !in_array((int) $user->level_id, [5, 6], true)) {
            return abort(403);
        }

        // Logic reset: truncate all tables and re-seed default data
        // WARNING: This is destructive
        
        DB::statement('SET FOREIGN_KEY_CHECKS=0;');
        
        $tables = DB::select('SHOW TABLES');
        foreach ($tables as $table) {
            foreach ($table as $key => $value) {
                if ($value == 'levels' || $value == 'migrations') continue; // Don't wipe levels or migrations
                DB::table($value)->truncate();
            }
        }
        
        // Re-seed default Super Admin if needed, or redirect to login
        // For now, let's keep the current user as super admin
        DB::table('users')->insert([
            'name' => 'Super Admin',
            'email' => $user->email, // Keep current email
            'password' => $user->password, // Keep current password
            'level_id' => 6,
            'is_verified' => 1,
            'created_at' => now(),
            'updated_at' => now()
        ]);

        DB::statement('SET FOREIGN_KEY_CHECKS=1;');

        return back()->with('success', 'Database has been reset to initial state.');
    }

    public function restoreDatabase(Request $request)
    {
        // Only Super Admin
        $userId = session('id');
        $user = DB::table('users')->where('id', $userId)->first();
        if (!$user || !in_array((int) $user->level_id, [5, 6], true)) {
            return abort(403);
        }

        $request->validate([
            'backup_file' => 'required|file|mimes:sql,txt'
        ]);

        try {
            $file = $request->file('backup_file');
            $sql = file_get_contents($file->getRealPath());

            // Split SQL by semicolon to execute commands individually if needed, 
            // or use DB::unprepared for raw execution
            
            // Note: DB::unprepared is risky if SQL source is untrusted, 
            // but here it's from Super Admin
            
            DB::unprepared($sql);

            return back()->with('success', 'Database restored successfully from backup.');
        } catch (\Exception $e) {
            return back()->with('error', 'Restore failed: ' . $e->getMessage());
        }
    }

